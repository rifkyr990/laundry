        <p>Bisa dihilangkan dengan bahan alami lho, Moms!</p>
        <p><img src="https://cdf.orami.co.id/unsafe/cdn-cas.orami.co.id/parenting/images/cara-menghilangkan-bau-apek-pad.width-800.jpegquality-80_2KAK2Sk.jpg"
