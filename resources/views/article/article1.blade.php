        <p>Bila Moms merasa wanginya sudah berkurang, bisa menggunakan cara yang sama kembali. Dengan begitu, pakaianmu
            pun akan senantiasa harum dan bebas bau apek.</p>
        <h3 id="gunakan-potpourri"><strong>7. Gunakan Potpourri</strong></h3>
        <p><img src="https://cdn-cas.orami.co.id/parenting/images/potpourri.width-1000.png" alt="potpourri"
                title="potpourri"></p>
        <p>Foto: potpourri</p>
        <p><em>Foto: <a href="http://thespruce.com">thespruce.com</a></em></p>
        <p>Bagi Moms yang belum tahu, potpourri adalah campuran rempah-rempah dan kelopak bunga kering.</p>
        <p>Potpourri biasa digunakan sebagai <a
                href="https://www.orami.co.id/magazine/amankah-menggunakan-pengharum-ruangan-dalam-kamar-bayi">pengharum
                ruangan</a> dan bisa juga digunakan sebagai cara menghilangkan bau apek pada baju.</p>
        <p>Moms di rumah bisa membuat sendiri campuran ini atau membeli dalam bentuk kemasan jadi.</p>
        <p>Cara menggunakannya cukup simpan campuran <a
                href="https://www.orami.co.id/magazine/manfaat-rempah-rempah">rempah</a> ini di dalam mangkuk atau
            kantong kasa. Lalu letakan di dalam lemari. Kemudian ganti atau aduk campuran, untuk membuat potpourri tetap
            segar.</p>
        <h2 id="cara-mencegah-bau-apek-pada-baju-di-dalam-lemari"><strong>Cara Mencegah Bau Apek pada Baju di Dalam
                Lemari</strong></h2>
        <p>Baju yang terlalu lama disimpan di lemari, juga bisa mengeluarkan bau apek. Apalagi jika Moms memiliki rumah
            yang lembab atau tinggal di daerah dengan iklim dingin.</p>
        <p>Selain memberikan kapur barus atau anti lembap di dalam lemari.</p>
        <p>Moms juga bisa mengunakan beberapa cara menghilangkan bau apek pada baju di lemari berikut ini.</p>
        <h3 id="bersihkan-lemari-secara-rutin"><strong>1. Bersihkan Lemari Secara Rutin</strong></h3>
        <p><img src="https://cdn-cas.orami.co.id/parenting/images/3_Lemari_Baju_Bayi_-2.width-1000.jpg"
                alt="Cara menghilangkan bau apek pada baju dengan membersihkan lemari"
                title="Cara menghilangkan bau apek pada baju dengan membersihkan lemari"></p>
        <p>Foto: Cara menghilangkan bau apek pada baju dengan membersihkan lemari</p>
        <p><em>Foto: Orami Photo Stock</em></p>
        <p>Salah satu <a href="https://www.orami.co.id/magazine/lemari-baju-bayi">penyebab bau apek di lemari</a> adalah
            bakteri yang ada di lemari. Pastikan Moms selalu membersihkan lemari secara rutin minimal satu bulan sekali
            ya.</p>
        <p>Bersihkan lemari dengan kain basah yang sudah direndam dalam campuran air panas dan cuka. Pastikan semua
            bagian sudah dibersihkan secara merata.</p>
        <p>Jangan ragu untuk menggunakan pembersih jamur atau antiseptik untuk menggilangkan bakteri agar lemari selalu
            terjaga kebersihannya dan bau terhindar dari bau apek.</p>
